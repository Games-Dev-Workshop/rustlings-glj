fn main() {
    let x;
    x = 5;
    if x == 10 {
        println!("x is ten!");
    } else {
        println!("x is not ten!");
    }

    let y:i8 = 4;

    if y == 4 {
        println!("y is 4, look -->{}", y);
    } else {
        println!("y is not 4, look -->{}", y);
    }
}
